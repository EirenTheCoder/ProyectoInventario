    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        s.setNumSalida(txtnumSalida.getText());
        s.setIdCliente(Integer.parseInt(txtidCliente.getText()));
        s.setSubttotal(Double.parseDouble(txtsubtotal.getText()));
        s.setIgv(Double.parseDouble(txtigv.getText()));
        s.setTotal(Double.parseDouble(txttotal.getText()));
        Calendar cal;
        int d,m,a;
        cal=jcfechaSalida.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR)-1900;
        s.setFecha(new Date(a,m,d));
        if(daoS.insertar(s)){
            guardarDetalleSalida();
            JOptionPane.showMessageDialog(null, "Salida de producto registrada con éxito");
            calculaStock();
            numSalida();
            limpiarDataProd();
            limpiarDataCliente();
            txttotal.setText("");
            txtsubtotal.setText("");
            txtigv.setText("");
            limpiarTablaSalida();
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo registrar la salida");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

