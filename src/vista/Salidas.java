    private void agregarEntrada(){
        double precio,total,importe;
        modeloSalida=(DefaultTableModel) tablaSalidas.getModel();
        int idEntrada=Integer.parseInt(txtidEntrada.getText());
        int idSalida=Integer.parseInt(txtnumSalida.getText());
        int cantidad=Integer.parseInt(txtcantidad.getText());
        precio=Double.parseDouble(txtprecio.getText());
        String producto=txtproducto.getText();
        importe=cantidad*precio;
        int stock=Integer.parseInt(txtstock.getText());
        ArrayList lista=new ArrayList();
        if(stock>0 && cantidad<=stock){
            lista.add(idSalida);
            lista.add(idEntrada);
            lista.add(producto);
            lista.add(precio);
            lista.add(cantidad);
            lista.add(importe);
            Object[] ob=new Object[6];
            ob[0]=lista.get(0);
            ob[1]=lista.get(1);
            ob[2]=lista.get(2);
            ob[3]=lista.get(3);
            ob[4]=lista.get(4);
            ob[5]=lista.get(5);
            modeloSalida.addRow(ob);
            tablaSalidas.setModel(modeloSalida);
        }else{
            JOptionPane.showMessageDialog(null, "No hay suficiente stock");
        }
  
    }
    
    void guardarDetalleSalida(){
        for(int i=0;i<tablaSalidas.getRowCount();i++){
            int idSalida=Integer.parseInt(tablaSalidas.getValueAt(i, 0).toString());
            int idEntrada=Integer.parseInt(tablaSalidas.getValueAt(i, 1).toString());
            int cantidad=Integer.parseInt(tablaSalidas.getValueAt(i, 4).toString());
            Double importe=Double.parseDouble(tablaSalidas.getValueAt(i, 5).toString());
            daoDs.insertar(idSalida, idEntrada, cantidad, importe);
        }
    }
    
    void calculaStock(){
        for(int i=0;i<tablaSalidas.getRowCount();i++){
            int idEntrada=Integer.parseInt(tablaSalidas.getValueAt(i, 1).toString());
            int cantidad=Integer.parseInt(tablaSalidas.getValueAt(i, 4).toString());
            daoS.calcularStock(idEntrada, cantidad);
        }
    }
    
    void calcularTotal(){
        double fila,igv,subtotal,total = 0;
        for(int i=0;i<tablaSalidas.getRowCount();i++){
            fila=Double.parseDouble(tablaSalidas.getValueAt(i, 5).toString());
            total+=fila;
            igv=total*0.18;
            subtotal=total-igv;
            txttotal.setText(String.format("%.2f", total).replace(",", "."));
            txtsubtotal.setText(String.format("%.2f", subtotal).replace(",", "."));
            txtigv.setText(String.format("%.2f", igv).replace(",", "."));
        }
        
    }
    
